    function handleScroll() {
        let currentSection = '';
        const scrollTop = window.scrollY || document.documentElement.scrollTop;
        
        // Auto-hide menu on scroll down, show on scroll up
        if (scrollTop > lastScrollTop && scrollTop > 100) {
            // Scrolling down - hide menu
            navMenu.classList.remove('active');
            document.getElementById('menuIcon').textContent = '☰';
        } else if (scrollTop < lastScrollTop) {
            // Scrolling up - show menu if user has scrolled more than 100px
            if (scrollTop > 100) {
                navMenu.classList.add('active');
            }
        }
        lastScrollTop = scrollTop <= 0 ? 0 : scrollTop; // For mobile or negative scrolling
        sections.forEach(section => {
            const sectionTop = section.offsetTop;
            const sectionHeight = section.clientHeight;
            if (scrollTop >= sectionTop - sectionHeight / 3) {
                currentSection = section.getAttribute('id');
            }
        });

        indicators.forEach(indicator => {
            indicator.classList.remove('active');
            if (indicator.getAttribute('data-section') === currentSection) {
                indicator.classList.add('active');
            }
        });

        // Smart auto-hide scroll indicators logic like v1
        const atTop = scrollTop < 100;
        const atBottom = (window.innerHeight + scrollTop) >= document.body.offsetHeight - 100;
        const hasActiveIndicator = document.querySelector('.scroll-indicator.active');
        
        // Show indicators when:
        // 1. Not at top/bottom
        // 2. Has an active section indicator  
        // 3. User is in the middle of the page
        const shouldShow = !atTop && !atBottom && hasActiveIndicator && scrollTop > window.innerHeight / 3;
        
        if (shouldShow) {
            scrollIndicators.classList.add('visible');
            scrollIndicators.classList.remove('hidden');
        } else {
            scrollIndicators.classList.add('hidden');
            scrollIndicators.classList.remove('visible');
        }
    }

