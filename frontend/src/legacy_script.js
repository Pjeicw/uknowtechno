    function debounce(func, wait) {
        let timeout;
        return function executedFunction(...args) {
            const later = () => {
                clearTimeout(timeout);
                func(...args);
            };
            clearTimeout(timeout);
            timeout = setTimeout(later, wait);
        };
    }
    
    function init3D() {
        const container = document.querySelector('.hero-3d');
        const fallback = document.querySelector('.hero-3d .fallback-content');
        if (!container || !window.THREE) {
            if (fallback) fallback.style.display = 'block';
            console.warn('3D initialization failed: container or THREE.js not found');
            return;
        }
        if (container.offsetWidth === 0 || container.offsetHeight === 0) {
            console.warn('Container has invalid dimensions, retrying...');
            setTimeout(init3D, 100);
            return;
        }
        try {
            scene = new THREE.Scene();
            camera = new THREE.PerspectiveCamera(75, container.offsetWidth / container.offsetHeight, 0.1, 1000);
            renderer = new THREE.WebGLRenderer({ alpha: true, antialias: true });
            renderer.setSize(container.offsetWidth, container.offsetHeight);
            renderer.setClearColor(0x000000, 0);
            container.appendChild(renderer.domElement);
            
            const geometry = new THREE.BoxGeometry(2, 2, 2);
            const material = new THREE.MeshBasicMaterial({
                color: 0x64FFDA,
                wireframe: true,
                transparent: true,
                opacity: 0.6
            });
            cube = new THREE.Mesh(geometry, material);
            scene.add(cube);
            
            const glowGeometry = new THREE.BoxGeometry(2.2, 2.2, 2.2);
            const glowMaterial = new THREE.MeshBasicMaterial({
                color: 0x64FFDA,
                transparent: true,
                opacity: 0.1
            });
            const glowCube = new THREE.Mesh(glowGeometry, glowMaterial);
            scene.add(glowCube);
            
            camera.position.z = 5;
            
            let mouseX = 0, mouseY = 0;
            document.addEventListener('mousemove', (event) => {
                mouseX = (event.clientX / window.innerWidth) * 2 - 1;
                mouseY = -(event.clientY / window.innerHeight) * 2 + 1;
            });
            
            function animate() {
                animationFrameId = requestAnimationFrame(animate);
                if (cube && glowCube) {
                    cube.rotation.x += 0.003 + mouseY * 0.0003; // Slower rotation as requested
                    cube.rotation.y += 0.003 + mouseX * 0.0003; // Slower rotation as requested
                    glowCube.rotation.x = cube.rotation.x;
                    glowCube.rotation.y = cube.rotation.y;
                }
                if (renderer && scene && camera) {
                    renderer.render(scene, camera);
                }
            }
            animate();
            
            window.addEventListener('resize', debounce(() => {
                if (camera && renderer && container) {
                    camera.aspect = container.offsetWidth / container.offsetHeight;
                    camera.updateProjectionMatrix();
                    renderer.setSize(container.offsetWidth, container.offsetHeight);
                }
            }, 100));
        } catch (error) {
            console.error('Error initializing 3D scene:', error);
            if (fallback) fallback.style.display = 'block';
        }
    }
