    function initTypingAnimation(element, text) {
        // Clear any existing animation
        if (typingInterval) {
            clearTimeout(typingInterval);
        }
        
        let i = 0;
        let isDeleting = false;
        element.textContent = '';
        
        function type() {
            if (!isDeleting) {
                // Typing forward
                if (i < text.length) {
                    element.textContent = text.slice(0, i + 1);
                    i++;
                    typingInterval = setTimeout(type, 150); // Slower typing
                } else {
                    // Pause when finished typing
                    typingInterval = setTimeout(() => {
                        isDeleting = true;
                        type();
                    }, 2000); // Pause for 2 seconds
                }
            } else {
                // Deleting backward
                if (i > 0) {
                    element.textContent = text.slice(0, i - 1);
                    i--;
                    typingInterval = setTimeout(type, 100); // Faster deleting
                } else {
                    // Finished deleting, start over
                    isDeleting = false;
                    typingInterval = setTimeout(type, 500); // Pause before restarting
                }
            }
        }
        
        // Start typing after initial delay
        typingInterval = setTimeout(type, 1000);
    }
    
